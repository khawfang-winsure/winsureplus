-- 0009 — ค่าคอมหาร้าน (ใครเป็นคนดึงร้านนี้เข้ามา + หาเมื่อไหร่)
-- ก๊อปไปวางใน SQL Editor ของ Supabase แล้วกด Run (รันได้แม้รัน 0001-0008 ไปแล้ว)
--
-- ปลอดภัยมาก: เพิ่มคอลัมน์ล้วนๆ 2 ช่องในตารางร้าน ไม่แตะของเดิม
--   recruited_by = พนักงานที่หาร้านนี้ (อ้างอิงตาราง profiles; ลบพนักงานแล้วช่องนี้เป็น null ร้านไม่พัง)
--   recruited_at = วันที่หาร้านนี้ได้ (ใช้คิดทั้งค่าคอมก้อนหาร้าน + กรอบเวลาโบนัส)
-- ============================================================================

alter table shops
  add column if not exists recruited_by  uuid references profiles (id) on delete set null,
  add column if not exists recruited_at  date;

--
-- commission_rate_locked  = เรตค่าคอม (บาท/เคส) ที่แช่แข็งไว้ตอนปิดยอด (null = ยังไม่ปิด)
-- commission_locked_month = เดือนที่ปิดยอด เช่น '2026-06' (null = ยังไม่ปิด)
-- เหตุผล: กันยอดเดือนเก่าเพี้ยน เวลาแอดมินไปแก้ขั้นค่าคอมทีหลัง
